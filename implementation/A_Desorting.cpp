void solve()
{
    int n; cin >> n;
    vector<int> a(n); take(a, n); // input vector
    
    vector<int> diff;  // array to store difference of consecutive elements

    for(int i = 0; i<n-1; i++)
    {
        int d = a[i+1] - a[i];
        if(d < 0) {        // if diff is negative means already unsorted
            cout << 0 << nl;
            return;    // return to the new testcase
        }
        else diff.push_back(a[i+1] - a[i]);
    }

    int ans = 1e11;
    for(int i = 0; i < n-1; i++)
    {
        if(diff[i] == 0){ // if difference of two consecutive elems is 0 then we only need one operation
            ans = 1;
            break;
        }
        else ans = min(ans, (diff[i]) / 2 + 1);  // suppose diff is 5 then we need 1 + 5/2 = 3 operations
    }
    cout << ans << endl;
}

