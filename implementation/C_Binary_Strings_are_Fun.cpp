long long binpow(long long a, long long b, long long m) {
    a %= m;
    long long res = 1;
    while (b > 0) {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

const int M = 998244353 ;

void solve()
{
    int n; cin >> n;
    string s; cin >> s;

    int ans = 0, cnt = 0;
    char p = 'a';
    for(int i=0; i<n; i++){
        if(s[i] != p){
            cnt = 1;
            ans += 1;
            ans %= M;
            p = s[i];
        }
        else{
            ans = (ans + binpow(2, cnt, M)) % M;
            cnt++;
        }
    }
    cout << ans << endl;
    
}

