int four(int n){
    while(n > 0){
        int dig = n%10;
        if(dig == 4) return true;
        n /= 10;
    }
    return false;
}

int dp[15];

int f(int n)
{
    int digits = to_string(n).size();
    if(dp[digits] != -1) return dp[digits];

    if(digits < 3){
        int ans = 0;
        for(int i=1; i<=n; i++){
            if(four(i)) ans++ ;
        }
        return ans;
    }

    if(digits == 3){
        return dp[digits] = 10 + 9 ;
    }

    return dp[digits] = pow(10, digits-2) + 9 * f(pow(10, digits-2)) ;
}

int calcremove(int dig)
{
    int ans = 0;
    int factor = 1;
    while(dig > 0){
        int last = dig % 10;
        if(factor <= 10)
            ans += f( factor * last ) ;
        else
            ans += last * f(factor) ;

        factor *= 10;
        dig /= 10 ;
    }
    return ans;
}

void solve()
{
    int n; cin >> n;
    int dig = n;
    memset(dp, -1, sizeof dp);

    int remove = calcremove(n);
   
    int newval = n + remove;

    int more = calcremove(newval) - remove ;

    int final = newval + more ;

    cout << final << endl;
}

