long long binpow(long long a, long long b) {
    
    long long res = 1;
    while (b > 0) {
        if (b & 1)
            res = res * a ;
        a = a * a ;
        b >>= 1;
    }
    return res;
}

int gx(int x){
    int l = 1, r = 11, z = 1;
    int y = log2(x);
    while(l <= r){
        int mid = (l+r)/ 2;
        if(binpow(y, mid) <= x){
            z = mid;
            l = mid+1;
        }
        else r = mid-1;
    }
    return z;
}

int divide(int l, int r){
    int lef = gx(l), rig = gx(r);
    if(lef == rig) return ((r - l + 1)%M * lef % M)%M ;
    
    int mid = (l + r) / 2 ;
    return (divide(l, mid)%M  + divide(mid+1, r)%M) % M ;
}

