const int N = 40;
vpi coeff(N);

void findcoeff()
{
    coeff[1].ff = 1; coeff[1].ss = 0; coeff[2].ff = 0; coeff[2].ss = 1;

    for(int i = 3; i<N; i++){
        int a1 = coeff[i-2].ff, a2 = coeff[i-1].ff;
        int b1 = coeff[i-2].ss, b2 = coeff[i-1].ss;

        coeff[i].ff = a1 + a2;
        coeff[i].ss = b1 + b2;
    }
}

