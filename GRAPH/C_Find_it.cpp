vi g[N];
vector<bool> v(N, false);

vi ans;

void DFS(int i){
    v[i] = 1;
    for(auto e : g[i]){
        if(v[e]) {
            int cur = e;
            ans.pb(cur);
            int child = g[cur][0];
            
            while(child != cur){
                ans.pb(child);
                int temp = g[child][0];
                child = temp;
            }
            if(child == cur) return;
        }

        DFS(e);
    }
}

