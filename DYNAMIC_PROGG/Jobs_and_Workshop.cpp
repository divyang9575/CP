int dp[N][1<<21];
int func(int i, int mask, int n)
{
    if(i == n+1) return 0;

    if(dp[i][mask] != -1) return dp[i][mask];

    int ans = 1e10;
    for(int j = 0; j < n; j++){
        if(mask & (1<<j))
        ans = min(ans, cost[i][j] + func(i+1, mask ^ (1<<j), n));
    }
    return dp[i][mask] = ans;
}

